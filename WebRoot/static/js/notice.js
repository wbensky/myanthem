$(document).ready(function(){
    var notice_id = $.cookie("notice_id");
    $.ys_ajax({
        url:"notice/get",
        datatype:"JSON",
        type:"POST",
        data:{
            id:notice_id
        },
        rt_func:function(data){
            var target = $(".sub-page-content .content .right");
            target.find(".content-nav").html($("<a></a>").html(data.notice.title));
            target.find(".content").html(data.notice.content);
        }
    });
});
